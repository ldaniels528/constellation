package constellation.model;

/**
 * Represents the enumeration of Drafting Standards
 * @author lawrence.daniels@gmail.com
 */
public enum DraftingStandards {
	STD_DEFAULT,	// No standard specified 
	STD_ISO, 		// International Organization for Standardization
	STD_AFNOR, 		// French Association for Standardization
	STD_ANSI, 		// American National Standards Institute 
	STD_BSI, 		// British Standards Institute
	STD_CSA, 		// Canadian Standards Association
	STD_DIN, 		// German Institute for Standardization
	STD_JIS 		// Japanese_Institute_for_Standardization
	
}
